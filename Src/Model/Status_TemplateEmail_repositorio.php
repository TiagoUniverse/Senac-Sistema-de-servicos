namespace model;

use PDOException;

class Status_TemplateEmail_repositorio
{

    /*
    * ┌───────────────────────────────────────────────────────────────────────────────────────────────────────────────┐
    * │  Função para listar todos os serviços já criados                                                              │
    * └───────────────────────────────────────────────────────────────────────────────────────────────────────────────┘
    */
    function listar_Status($pdo)
    {
        try {
            $stmt = $pdo->prepare("Select * from Status_TemplateEmail ");

            $stmt->execute();

            $lista_status = array();
            while ($linha = $stmt->fetch(\PDO::FETCH_ASSOC)) {
                $idStatus = $linha['id'];
                $nome = $linha['nome'];

                $lista_status[] = array($idStatus, $nome);
            
            }
            return $lista_status;
        } catch (PDOException $err) {
            echo $err->getMessage();
        }
    }


    
}
